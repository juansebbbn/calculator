    private void boton_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_igualActionPerformed
        double segundo_numero;
        if(operacion.equals("nula")){
            etiqueta_numeros.setText(cadena_numeros);
        }else if(operacion.equals("sumar")){
            segundo_numero = Double.parseDouble(cadena_numeros);
            resultado = primer_numero + segundo_numero;
            entero = esEntero(resultado, epsilon);
            if(entero){
                 etiqueta_numeros.setText(String.format("% .0f", resultado));
            }
            cadena_numeros = String.valueOf(resultado);
            operacion = "nula";
        }else if(operacion.equals("multiplicar")){
            segundo_numero = Double.parseDouble(cadena_numeros);
            resultado = primer_numero * segundo_numero;
            entero = esEntero(resultado, epsilon);
            if(entero){
                 etiqueta_numeros.setText(String.format("% .0f", resultado));
            }
            cadena_numeros = String.valueOf(resultado);
            operacion = "nula";
        }else if(operacion.equals("dividir")){
            segundo_numero = Double.parseDouble(cadena_numeros);
            resultado = primer_numero / segundo_numero;
            entero = esEntero(resultado, epsilon);
            if(entero){
                 etiqueta_numeros.setText(String.format("% .0f", resultado));
            }
            cadena_numeros = String.valueOf(resultado);
            operacion = "nula";
        }else if(operacion.equals("restar")){
            segundo_numero = Double.parseDouble(cadena_numeros);
            resultado = primer_numero - segundo_numero;
            entero = esEntero(resultado, epsilon);
            if(entero){
                 etiqueta_numeros.setText(String.format("% .0f", resultado));
            }
            cadena_numeros = String.valueOf(resultado);
            operacion = "nula";
        }else if(operacion.equals("raiz")){
            resultado = Math.sqrt(primer_numero);
            entero = esEntero(resultado, epsilon);
            if(entero){
                 etiqueta_numeros.setText(String.format("% .0f", resultado));
            }
            cadena_numeros = String.valueOf(resultado);
            operacion = "nula";
        }
        
        etiqueta_muestra.setText("");
        activado = true;
    }//GEN-LAST:event_boton_igualActionPerformed

